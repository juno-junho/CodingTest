import java.util.Scanner;

public class Goldbach {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int testCase = sc.nextInt();
        for (int i = 0; i < testCase ; i++) {
            int num = sc.nextInt();
            if (num % 2 != 0) break;
            // 두 수가 소수
        }
    }
}
